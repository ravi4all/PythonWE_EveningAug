# range(start,stop,step)
# start - 0 by default, from where loop will start
# stop - till which index loop will execute
# step - 1 by default, how many steps to take in single iteration

for i in range(5):
    print(i)

for i in range(4,17,4):
    print(i)
    print("Inside Loop")

print("Bye Loop")

